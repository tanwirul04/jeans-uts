    private void initTabelKeranjang(){
        modelKeranjang = new DefaultTableModel(
            new Object[]{"ID Produk", "Nama Produk", "Harga", "Jumlah", "Total Harga"}, 0
        );
        tabelKeranjang.setModel(modelKeranjang);
    }

    private void hitungTotal() {
        int total = 0;
        
        for (int i = 0; i < modelKeranjang.getRowCount(); i++) {
        total += Integer.parseInt(
            modelKeranjang.getValueAt(i, 4).toString()
        );
    }

    txtTotal.setText(String.valueOf(total));
       
    }
    
    public void RefreshData(){
        try {
            tblNamaBrg.setRowHeight(30);
            DefaultTableModel model = (DefaultTableModel) tblNamaBrg.getModel();
            
            for (int i = model.getRowCount()-1; i>=0; i--) {
                model.removeRow(i);   
            }
            
            String Q = "SELECT * FROM produk";
            Connection C = config.configDB();
            Statement S = C.createStatement();
            ResultSet R = S.executeQuery(Q);
            while (R.next()){
                int ID = R.getInt("id_produk");
                String  PRODUK = R.getString("produk");
                String HARGA = R.getString("harga");
                String STOCK = R.getString("stock");
                Object[] datausers = {ID, PRODUK, HARGA, STOCK};
                model.addRow(datausers);
            }
            DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
            centerRenderer.setHorizontalAlignment(SwingConstants.CENTER);
            for (int i = 0; i < tblNamaBrg.getColumnCount(); i++) {
            tblNamaBrg.getColumnModel().getColumn(i).setCellRenderer(centerRenderer);
        }
        } catch (Exception e){
            e.printStackTrace();
            
        }
        
    }
