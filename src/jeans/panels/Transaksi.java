    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:
        if (Bg == null) {
        JOptionPane.showMessageDialog(this, "Pilih arang dulu!");
        return;
    }

    int jumlah = Integer.parseInt(txtJumlah.getText());
    int harga = Integer.parseInt(Bg.getHarga());
    int total = harga * jumlah;

    modelKeranjang.addRow(new Object[]{
        Bg.getId(),
        Bg.getProduk(),
        harga,
        jumlah,
        total
    });
    
    hitungTotal();
    btnTambah.setEnabled(false);
    Bg = null;   
    }//GEN-LAST:event_btnTambahActionPerformed

    private void tblNamaBrgMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblNamaBrgMouseClicked
        // TODO add your handling code here:
        int n = tblNamaBrg.getSelectedRow();
        if(n != -1){
            btnTambah.setEnabled(true);
            
            Object IDBrg = tblNamaBrg.getValueAt(n, 0);
            int ID = Integer.parseInt(IDBrg.toString());
            String produkBr = tblNamaBrg.getValueAt(n, 1).toString();
            String hargaBr = tblNamaBrg.getValueAt(n, 2).toString();
            String stockBr = tblNamaBrg.getValueAt(n, 3).toString();
            Bg = new Produk();
            Bg.setId(ID); 
            Bg.setProduk(produkBr); 
            Bg.setHarga(hargaBr);
            Bg.setStock(stockBr);
        }else {
        }
    }//GEN-LAST:event_tblNamaBrgMouseClicked

